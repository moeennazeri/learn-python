# 🌀 آموزش حلقه‌ها در پایتون (for و while)

# 🔸 حلقه for: برای تکرار روی لیست، رشته، یا محدوده (range)

# مثال ۱: چاپ اعداد از ۱ تا ۵
for i in range(1, 6):
    print("عدد:", i)

# مثال ۲: چاپ اعضای یک لیست
fruits = ["سیب", "موز", "هندوانه"]

for fruit in fruits:
    print("میوه:", fruit)

# مثال ۳: چاپ حروف یک رشته
name = "مبین"

for letter in name:
    print("حرف:", letter)

# --------------------------------------------------------

# 🔸 حلقه while: تا زمانی که شرط درست باشد، حلقه ادامه دارد

# مثال ۴: چاپ اعداد تا زمانی که کمتر از ۵ باشند
counter = 0
while counter < 5:
    print("شمارنده:", counter)
    counter += 1

# --------------------------------------------------------

# 🔸 استفاده از break برای توقف حلقه

# مثال ۵: توقف در عدد ۳
for i in range(10):
    if i == 3:
        break
    print("i =", i)

# --------------------------------------------------------

# 🔸 استفاده از continue برای رد کردن یک تکرار

# مثال ۶: فقط چاپ اعداد فرد
for i in range(1, 6):
    if i % 2 == 0:
        continue
    print("عدد فرد:", i)
